package exercicios;

public class Exercicio01 {
    public static void main(String[] args) {
        int cont;

        cont = 100;
        while (cont<=200) {
            System.out.print(cont + " ");

            cont=cont+1;
        }

        System.out.printf("\n");
        System.out.println("Final da contagem");
    }
}
